// CORPUS REGISTRY
// ═══════════════════════════════════════════════════════════════════

const CORPORA: Record<string, FrequencyCorpus> = {
  fr: { language: "fr", words: FRENCH_FREQUENCY_WORDS },
  de: { language: "de", words: GERMAN_FREQUENCY_WORDS },
  it: { language: "it", words: ITALIAN_FREQUENCY_WORDS },
};

/**
 * Get frequency corpus for a language.
 * Falls back to French if language not found.
 */
export function getFrequencyCorpus(language: string): FrequencyCorpus {
  return CORPORA[language] ?? CORPORA["fr"];
}

/**
 * Get the initial 10 words that must be introduced first.
 */
export function getInitialWords(language: string): FrequencyCorpusWord[] {
  const corpus = getFrequencyCorpus(language);
  return corpus.words.filter((w) => w.isInitialWord === true);
}

/**
 * Get the next N words to introduce by frequency rank,
 * excluding words the learner already knows.
 */
export function getNextWordsToIntroduce(
  language: string,
  knownLemmas: Set<string>,
  count: number,
): FrequencyCorpusWord[] {
  const corpus = getFrequencyCorpus(language);
  return corpus.words
    .filter((w) => !knownLemmas.has(w.lemma.toLowerCase()))
    .sort((a, b) => a.rank - b.rank)
    .slice(0, count);
}
