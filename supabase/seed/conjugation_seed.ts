  const forms: VerbSeedData["forms"] = [];

  const addForms = (
    tense: string,
    mood: string,
    conjugations: string[],
    explanationKey?: string,
  ) => {
    conjugations.forEach((form, i) => {
      forms.push({
        tense,
        mood,
        pronoun: pronouns[i],
        pronoun_key: keys[i],
        conjugated_form: form,
        rule_explanation: explanationKey
          ? explanations?.[explanationKey]
          : undefined,
      });
    });
  };

  addForms("present", "indicative", [...present], "present");
  addForms("imperfect", "indicative", [...imperfect], "imperfect");
  addForms("perfect", "indicative", [...perfect], "perfect");
  addForms("future", "indicative", [...future], "future");
  addForms("conditional", "indicative", [...conditional], "conditional");

  return {
    language: "fr",
    infinitive,
    english_meaning: english,
    verb_class: verbClass,
    frequency_rank: rank,
    tags,
    forms,
  };
