// Helper to generate all 6 pronoun forms for a German verb
function deVerb(
  infinitive: string,
  english: string,
  verbClass: string,
  rank: number,
  tags: string[],
  present: [string, string, string, string, string, string],
  preterite: [string, string, string, string, string, string],
  perfect: [string, string, string, string, string, string],
  future: [string, string, string, string, string, string],
  subjunctiveII: [string, string, string, string, string, string],
  explanations?: Partial<Record<string, string>>,
): VerbSeedData {
  const pronouns = ["ich", "du", "er/sie/es", "wir", "ihr", "sie/Sie"];
  const keys = ["1sg", "2sg", "3sg", "1pl", "2pl", "3pl"] as const;

  const forms: VerbSeedData["forms"] = [];

  const addForms = (
    tense: string,
    mood: string,
    conjugations: string[],
    explanationKey?: string,
  ) => {
    conjugations.forEach((form, i) => {
      const ruleKey = explanationKey
        ? `${explanationKey}_${keys[i]}`
        : undefined;
      forms.push({
        tense,
        mood,
        pronoun: pronouns[i],
        pronoun_key: keys[i],
        conjugated_form: form,
        rule_explanation: ruleKey
          ? (explanations?.[ruleKey] ?? explanations?.[explanationKey!])
          : undefined,
      });
    });
  };

  addForms("present", "indicative", [...present], "present");
  addForms("preterite", "indicative", [...preterite], "preterite");
  addForms("perfect", "indicative", [...perfect], "perfect");
  addForms("future", "indicative", [...future], "future");
  addForms(
    "subjunctive_imperfect",
    "subjunctive",
    [...subjunctiveII],
    "subjunctive_ii",
  );

  return {
    language: "de",
    infinitive,
    english_meaning: english,
    verb_class: verbClass,
    frequency_rank: rank,
    tags,
    forms,
  };
}

